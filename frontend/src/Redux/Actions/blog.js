// Delete post
export const deletePost = (id) => async (dispatch) => {
  try {
    await axios.delete(`/app/blog/${id}`);

    dispatch({
      type: DELETE_POST,
      payload: id,
    });

    dispatch(SetAlert('Post Removed', 'success'));
  } catch (err) {
    dispatch({
      type: POST_ERROR,
      payload: { msg: err.response.statusText, status: err.response.status },
    });
  }
};

// Add post
export const addPost = (text) => async (dispatch) => {
  const config = {
    headers: {
      'Content-Type': 'application/json',
    },
  };

  try {
    const res = await axios.post('/app/blog', {text}, config);

    dispatch({
      type: ADD_POST,
      payload: res.data,
    });

    dispatch(SetAlert('Post Created', 'success'));
  } catch (err) {
    dispatch({
      type: POST_ERROR,
      payload: { msg: err.response.statusText, status: err.response.status },
    });
  }
};

